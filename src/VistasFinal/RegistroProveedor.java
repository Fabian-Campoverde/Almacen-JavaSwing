    private void registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroActionPerformed
        String RUC, raz, nom, dir;
        int tele;
        RUC = ruc.getText();
        
        raz = razon.getText();
        nom = nombre.getText();
        dir = direccion.getText();
        if (raz.equals("") || nom.equals("") || dir.equals("") || RUC.equals("") || telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingresar datos no vacios");
        } else {
            tele = Integer.parseInt(telefono.getText());
            ControladorProveedor.setDatosProv(RUC, raz, nom, dir, tele);
            String msg = ControladorProveedor.decisor(1);
            if (msg.equals("RUC ya existente")) {
                JOptionPane.showMessageDialog(null, msg);
            } else {
                JOptionPane.showMessageDialog(null, msg);
                ruc.setText("");
                razon.setText("");
                nombre.setText("");
                direccion.setText("");
                telefono.setText("");
                menu.actualizarTabla();
            }
        }
    }//GEN-LAST:event_registroActionPerformed

